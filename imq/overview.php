<h3 class="text-primary">ตัวอย่างการประกอบอาชีพหลังจบการศึกษา</h3>
<ul>
    <li>งานราชการ, รัฐวิสาหกิจ</li>
    <li>วิศวกรหรือผู้จัดการฝ่ายควบคุมคุณภาพ (Quality Control, QC Engineer or Quality
        Control Manager) </li>
    <li>วิศวกรผู้ดูแลระบบคุณภาพ (Quality System Engineer) </li>
    <li>ผู้ประสานงานด้านระบบคุณภาพ (Quality System Coordinator)
    </li>
    <li>วิศวกรควบคุมคุณภาพของผลิตภัณฑ์ใหม่ (Production Quality Engineer/ Project
        Quality Engineer)</li>
    <li>วิศวกรควบคุมคุณภาพของสินค้าวัตถุดิบ (Supplier Quality Engineer) </li>
    <li>วิศวกรควบคุมกระบวนการโดยใช้สถิติ (Statistical Process Control, SPC Engineer)</li>
    <li>วิศวกรด้านงานประกันคุณภาพ (Quality Assurance, QA Engineer)</li>
    <li> ผู้ดูแลระบบวัดคุมและเครื่องมือขั้นสูง (Instrument System Officer)</li>
    <li>ผู้ให้ค าปรึกษาด้านเครื่องมือ (Product Instrument Specialist)</li>
    <li>เจ้าหน้าที่สอบเทียบเครื่องมือวัดหรือนักวิชาการเกี่ยวกับงานรับรองมาตรฐานห้องปฏิบัติการ
        ทั้งภาครัฐและเอกชน (Calibration Officer or ISO Officer)</li>
    <li>ผู้ประกอบการด้านเครื่องมือ (Instrument Entrepreneur)</li>
</ul>



